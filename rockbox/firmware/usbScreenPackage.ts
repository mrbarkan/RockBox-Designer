const textEncoder = new TextEncoder();
const ZIP_DATE = new Date('1980-01-01T00:00:00.000Z');

const readU16 = (bytes: Uint8Array, offset: number) =>
  bytes[offset] | (bytes[offset + 1] << 8);

const readU32 = (bytes: Uint8Array, offset: number) =>
  (bytes[offset] | (bytes[offset + 1] << 8) | (bytes[offset + 2] << 16) | (bytes[offset + 3] << 24)) >>> 0;

const readI32 = (bytes: Uint8Array, offset: number) => {
  const value = readU32(bytes, offset);
  return value > 0x7fffffff ? value - 0x100000000 : value;
};

export const inspectUsbLogoBmp = (bytes: Uint8Array): UsbLogoInspection => {
  if (bytes.byteLength < 54) return { valid: false, error: 'The BMP header is incomplete.' };
  if (bytes[0] !== 0x42 || bytes[1] !== 0x4d) return { valid: false, error: 'The file is not a Windows BMP.' };
  const fileSize = readU32(bytes, 2);
  const pixelOffset = readU32(bytes, 10);
  const dibSize = readU32(bytes, 14);
  const width = readI32(bytes, 18);
  const height = readI32(bytes, 22);
  const planes = readU16(bytes, 26);
  const bitsPerPixel = readU16(bytes, 28);
  const compression = readU32(bytes, 30);
  const rowBytes = Math.ceil((width * bitsPerPixel) / 32) * 4;
  const requiredBytes = pixelOffset + rowBytes * Math.abs(height);

  const details = { width, height, bitsPerPixel, compression };
  if (dibSize < 40) return { valid: false, ...details, error: 'Use a Windows BMP with a 40-byte or newer DIB header.' };
  if (width !== 176 || height !== 48) return { valid: false, ...details, error: 'The iPod Video USB logo must be exactly 176 × 48 pixels.' };
  if (planes !== 1) return { valid: false, ...details, error: 'The BMP must contain one image plane.' };
  if (bitsPerPixel !== 24) return { valid: false, ...details, error: 'Export the logo as an uncompressed 24-bit BMP.' };
  if (compression !== 0) return { valid: false, ...details, error: 'Compressed BMP files are not supported by the pinned Rockbox bitmap tool.' };
  if (fileSize > bytes.byteLength || requiredBytes > bytes.byteLength) {
    return { valid: false, ...details, error: 'The BMP pixel data is truncated.' };
  }
  return { valid: true, ...details };
};

