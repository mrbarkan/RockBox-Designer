export const createUsbFirmwarePackage = async (options: UsbFirmwareOptions) => {
  const inspection = inspectUsbLogoBmp(options.logoBmp);
  if (!inspection.valid) throw new Error(inspection.error ?? 'The USB logo BMP is invalid.');
  const packageName = normalizePackageName(options.packageName) || 'rockbox-designer-usb';
  const zip = new JSZip();
  const files: FirmwarePackageFile[] = [];

  await addFile(zip, files, 'patches/usb-screen-layout.patch', createUsbScreenPatch(), 'patch');
  await addFile(zip, files, `overlay/${USB_HEADER_SOURCE_PATH}`, createUsbLayoutHeader(options.logoPosition), 'source-overlay');
  await addFile(zip, files, `overlay/${USB_LOGO_SOURCE_PATH}`, options.logoBmp, 'target-asset');
  await addFile(zip, files, 'scripts/verify.sh', createVerifyScript(), 'build-script', true);
  await addFile(zip, files, 'scripts/build-ipodvideo.sh', createBuildScript(), 'build-script', true);
  await addFile(zip, files, 'README.md', createReadme({ ...options, packageName }), 'documentation');
  await addFile(zip, files, 'COPYING-NOTICE.txt', createNotice(), 'documentation');

  const manifest: FirmwarePackageManifest = {
    schemaVersion: FIRMWARE_PACKAGE_SCHEMA_VERSION,
    mode: 'firmware',
    feature: 'usb-fallback-logo',
    packageName,
    target: {
      deviceProfileId: FIRMWARE_DEVICE_PROFILE_ID,
      rockboxTarget: FIRMWARE_TARGET,
      upstreamCommit: FIRMWARE_UPSTREAM_COMMIT
    },
    output: {
      kind: 'source-patch-package',
      ordinaryThemeInstall: false,
      containsRockboxSourceTree: false,
      containsRockboxDerivedPatch: true,
      containsGeneratedGplSource: true,
      containsRockboxBinary: false,
      containsProprietaryFirmware: false
    },
    usbFallback: {
      logoPosition: options.logoPosition,
      logoAssetPath: USB_LOGO_SOURCE_PATH,
      logoWidth: 176,
      logoHeight: 48,
      logoBitsPerPixel: 24
    },
    files: [...files].sort((left, right) => left.path.localeCompare(right.path))
  };
  zip.file('manifest.json', `${JSON.stringify(manifest, null, 2)}\n`, {
    date: ZIP_DATE,
    createFolders: true,
    unixPermissions: 0o100644
  });

  const bytes = await zip.generateAsync({
    type: 'uint8array',
    compression: 'DEFLATE',
    compressionOptions: { level: 9 },
    platform: 'UNIX'
  });
  return { bytes, filename: `${packageName}-firmware-${FIRMWARE_TARGET}.zip`, manifest };
};
