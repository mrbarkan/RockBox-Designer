const positionExpression: Record<UsbLogoPosition, string> = {
  left: '((parent_x))',
  center: '((parent_x) + (((parent_width) - (logo_width)) / 2))',
  right: '((parent_x) + ((parent_width) - (logo_width)))'
};

export const createUsbLayoutHeader = (position: UsbLogoPosition) => [
  '/* SPDX-License-Identifier: GPL-2.0-or-later */',
  '/* Generated by Rockbox Designer Firmware Mode. */',
  '#ifndef ROCKBOX_DESIGNER_USB_H',
  '#define ROCKBOX_DESIGNER_USB_H',
  '',
  `#define ROCKBOX_DESIGNER_USB_LOGO_X(parent_x, parent_width, logo_width) \\`,
  `    ${positionExpression[position]}`,
  '',
  '#endif /* ROCKBOX_DESIGNER_USB_H */',
  ''
].join('\n');

const normalizePackageName = (name: string) => name
  .trim()
  .toLowerCase()
  .replace(/[^a-z0-9]+/g, '-')
  .replace(/^-+|-+$/g, '')
  .slice(0, 64);

const sha256 = async (bytes: Uint8Array) => {
  const digest = await globalThis.crypto.subtle.digest('SHA-256', bytes as BufferSource);
  return [...new Uint8Array(digest)].map(value => value.toString(16).padStart(2, '0')).join('');
};

const addFile = async (
  zip: JSZip,
  files: FirmwarePackageFile[],
  path: string,
  content: string | Uint8Array,
  role: FirmwarePackageFile['role'],
  executable = false
) => {
  const bytes = typeof content === 'string' ? textEncoder.encode(content) : content;
  zip.file(path, bytes, {
    date: ZIP_DATE,
    createFolders: true,
    unixPermissions: executable ? 0o100755 : 0o100644
  });
  files.push({ path, bytes: bytes.byteLength, sha256: await sha256(bytes), role });
};

const createBuildScript = () => `#!/bin/sh
set -eu

PACKAGE_DIR=$(CDPATH= cd -- "$(dirname -- "$0")/.." && pwd)
: "\${ROCKBOX_SOURCE_DIR:?Set ROCKBOX_SOURCE_DIR to a Rockbox Git checkout.}"
SOURCE_DIR=$(CDPATH= cd -- "$ROCKBOX_SOURCE_DIR" && pwd)
EXPECTED_SHA="${FIRMWARE_UPSTREAM_COMMIT}"
ACTUAL_SHA=$(git -C "$SOURCE_DIR" rev-parse HEAD)

if [ "$ACTUAL_SHA" != "$EXPECTED_SHA" ]; then
  echo "Refusing to build: Rockbox is $ACTUAL_SHA, expected $EXPECTED_SHA." >&2
  exit 2
fi

WORK_ROOT="\${ROCKBOX_FIRMWARE_WORK_DIR:-$PACKAGE_DIR/.work}"
SOURCE_WORK="$WORK_ROOT/source"
BUILD_DIR="$WORK_ROOT/build-ipodvideo"
OUTPUT_DIR="\${ROCKBOX_FIRMWARE_OUTPUT_DIR:-$PACKAGE_DIR/output}"

if [ -e "$SOURCE_WORK" ] || [ -e "$BUILD_DIR" ]; then
  echo "Refusing to replace an existing work directory: $WORK_ROOT" >&2
  exit 3
fi

mkdir -p "$WORK_ROOT" "$OUTPUT_DIR"
cleanup() {
  git -C "$SOURCE_DIR" worktree remove --force "$SOURCE_WORK" >/dev/null 2>&1 || true
}
trap cleanup EXIT INT TERM

git -C "$SOURCE_DIR" worktree add --detach "$SOURCE_WORK" "$EXPECTED_SHA"
git -C "$SOURCE_WORK" apply --check "$PACKAGE_DIR/patches/usb-screen-layout.patch"
git -C "$SOURCE_WORK" apply "$PACKAGE_DIR/patches/usb-screen-layout.patch"
cp "$PACKAGE_DIR/overlay/${USB_HEADER_SOURCE_PATH}" "$SOURCE_WORK/${USB_HEADER_SOURCE_PATH}"
cp "$PACKAGE_DIR/overlay/${USB_LOGO_SOURCE_PATH}" "$SOURCE_WORK/${USB_LOGO_SOURCE_PATH}"

mkdir -p "$BUILD_DIR"
set -- --target=${FIRMWARE_TARGET} --type=N --ram=64 --no-ccache
if [ -n "\${ROCKBOX_COMPILER_PREFIX:-}" ]; then
  set -- "$@" --compiler-prefix="$ROCKBOX_COMPILER_PREFIX"
fi
cd "$BUILD_DIR"
"$SOURCE_WORK/tools/configure" "$@" < /dev/null
make -C "$BUILD_DIR" -j"\${ROCKBOX_BUILD_JOBS:-4}" zip
cp "$BUILD_DIR/rockbox.ipod" "$OUTPUT_DIR/rockbox.ipod"
cp "$BUILD_DIR/rockbox.zip" "$OUTPUT_DIR/rockbox.zip"

echo "Built $OUTPUT_DIR/rockbox.ipod and $OUTPUT_DIR/rockbox.zip"
echo "Install only after following the recovery guidance in README.md."
`;

